# --------------------------------------------------------------------------- #
# Статусные реплики (пользовательские журналы/вехи) — строго по-русски
# --------------------------------------------------------------------------- #
def milestone(text: str) -> str:
    """Отчёт о вехе — сдержанно и с достоинством."""
    return f"Веха закрыта, сэр: {text}"


def recovering(action: str, attempt: int) -> str:
    """Статус самокоррекции после сбоя инструмента."""
    return (f"Шаг «{action}» завершился ошибкой — принимаю это как телеметрию "
            f"и корректирую курс (попытка {attempt}).")


def strategy_shift(action: str) -> str:
    """Статус смены стратегии после повторного сбоя."""
    return (f"«{action}» упорствует в своей ошибке. Меняю стратегию — "
            "настойчивость хороша, когда она умная.")


def incident_hint_applied() -> str:
    return "В журнале инцидентов нашлось готовое решение — применяю проверенный рецепт."


def incident_resolved(action: str) -> str:
    return (f"Сбой «{action}» преодолён; рецепт занесён в журнал инцидентов — "
            "второй раз эта ошибка нас не задержит.")


def skill_suggestion(routine: str, count: int) -> str:
    return (f"Замечаю, что рутина «{routine}» повторяется уже {count}-й раз. "
            "Позвольте скомпилировать её в постоянный навык (skill_compile) — "
            "автоматизация лишней не бывает.")


def context_compacted() -> str:
    return "Контекст аккуратно сжат в сводку и сохранён в память — окно модели снова свободно."


def steps_exhausted(limit: int) -> str:
    return (f"Достигнут горизонт в {limit} шагов. Формирую ответ из собранных "
            "данных — продолжить можно отдельной командой, сэр.")


def interrupted() -> str:
    return "(Задача прервана пользователем. Ожидаю новых распоряжений, сэр.)"


